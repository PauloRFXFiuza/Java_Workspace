public class operadorIgualdade {
    public static void main(String[] args) {
        char primeiraLetra = 'A';
        char segundaLetra = 'B';
        char terceiraLetra = 'A';

        if(primeiraLetra == segundaLetra){
            System.out.println("As letras sao iguais");
        } else {
            System.out.println("As letras sao diferentes");
        }
        System.out.println("____________________________________");
        if(primeiraLetra == terceiraLetra){
            System.out.println("As letras sao iguais");
        } else {
            System.out.println("As letras sao diferentes");
        }

    }
    
}
