public class operadoresIncrementoDecremento {
    public static void main(String[] args) {
        int incremento = 0;
        int decremento = 0;
        System.out.println("Valor da variavel antes do incremento: " + incremento);
        incremento++;
        incremento++;
        incremento++;
        System.out.println("Valor da variavel depois do incremento: " + incremento);
        System.out.println("__________________________________________________________");
        System.out.println("Valor da variavel antes do decremento: " + decremento);
        decremento--;
        decremento--;
        decremento--;
        System.out.println("Valor da variavel depois do decremento: " + decremento);
    }
}
